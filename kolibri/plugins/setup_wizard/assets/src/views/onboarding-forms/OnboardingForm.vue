<template>

  <div>
    <h1 class="onboarding-form-header">
      {{ header }}
    </h1>

    <form @submit.prevent="$emit('submit')">
      <fieldset class="onboarding-form-fields">
        <legend class="onboarding-form-legend">
          <span v-if="hasDescription" class="onboardng-form-description">
            <slot name="description">{{ description }}</slot>
          </span>
          <span v-else class="visuallyhidden">{{ header }}</span>
        </legend>

        <slot></slot>
      </fieldset>

      <div class="form-footer">
        <slot name="footer"></slot>
      </div>

      <KButton
        class="onboarding-form-submit"
        :primary="true"
        type="submit"
        :text="submitText"
      />
    </form>
  </div>

</template>


<script>

  import KButton from 'kolibri.coreVue.components.KButton';

  export default {
    name: 'OnboardingForm',
    components: {
      KButton,
    },
    props: {
      header: {
        type: String,
        required: true,
      },
      description: {
        type: String,
        required: false,
      },
      submitText: {
        type: String,
        required: true,
      },
    },
    computed: {
      hasDescription() {
        return this.description || this.$slots.description;
      },
    },
  };

</script>


