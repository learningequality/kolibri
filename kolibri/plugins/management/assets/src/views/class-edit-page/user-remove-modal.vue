<template>

  <core-modal
    :title="$tr('modalTitle')"
    :hasError="false"
    @cancel="close"
  >
    <div>
      <p>{{ $tr('confirmation', { username: username, classname: classname }) }}</p>
      <p>{{ $tr('description') }} <strong>{{ $tr('users') }}</strong>.</p>

      <div class="core-modal-buttons">
        <k-button
          :text="$tr('cancel')"
          appearance="flat-button"
          @click="close"
        />
        <k-button
          :text="$tr('remove')"
          :primary="true"
          @click="userRemove"
        />
      </div>

    </div>
  </core-modal>

</template>


<script>

  import * as actions from '../../state/actions';
  import kButton from 'kolibri.coreVue.components.kButton';
  import coreModal from 'kolibri.coreVue.components.coreModal';

  export default {
    name: 'userRemoveModal',
    $trs: {
      modalTitle: 'Remove User from Class',
      remove: 'Remove from Class',
      cancel: 'Cancel',
      confirmation: "Are you sure you want to remove '{ username }' from '{ classname }'?",
      description: 'You can still access this account from the tab ',
      users: 'Users',
    },
    components: {
      kButton,
      coreModal,
    },
    props: {
      classname: {
        type: String,
        required: true,
      },
      username: {
        type: String,
        required: true,
      },
      classid: {
        type: String,
        required: true,
      },
      userid: {
        type: String,
        required: true,
      },
    },
    methods: {
      userRemove() {
        this.removeClassUser(this.classid, this.userid);
      },
      close() {
        this.displayModal(false);
      },
    },
    vuex: {
      actions: {
        removeClassUser: actions.removeClassUser,
        displayModal: actions.displayModal,
      },
    },
  };

</script>


